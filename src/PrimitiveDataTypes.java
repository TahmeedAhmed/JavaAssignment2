public class PrimitiveDataTypes {

    static boolean check = true;
    static byte myByte = 127;
    static char myChar = 's';
    static short myShort = 30000;
    static int myInt = 2147483647;
    static long myLong = 922337203685477580L;
    static float myFloat = 69f;
    static double myDouble = 100.024;

    public static void main(String[] args) {
        // boolean , byte , char , short , int , long , float and double
        int a = 343243;
        System.out.println(Integer.max(a, myInt));
    }
}
